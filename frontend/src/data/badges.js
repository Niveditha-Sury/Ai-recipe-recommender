// Helper to calculate locked/unlocked state
export const calculateUserBadges = (userXp = 0, userCookDays = 0) => {
    return BADGES.map((b) => {
        const isUnlocked =
            b.type === "xp"
                ? userXp >= b.threshold
                : userCookDays >= b.threshold;

        const progress =
            b.type === "xp"
                ? Math.min((userXp / b.threshold) * 100, 100)
                : Math.min((userCookDays / b.threshold) * 100, 100);

        return {
            ...b,
            isUnlocked,
            progress,
        };
    });
};
